<h2 class="pageID">Mobile Apps vs. Web Apps definitions: </h3>
 <p>
 <ul> <u>Mobile App</u> -
    <li>Mobile apps are built for a specific platform, such as iOS for the Apple iPhone or Android for a Samsung device. </li>
    <li>They are downloaded and installed via an app store and have access to system resources, such as GPS and the camera function.</li>
    <li>Mobile apps live and run on the device itself. </li>
    <li>Snapchat, Instagram, Google Maps and Facebook Messenger are some examples of popular mobile apps.</li>
    </ul>
</p>

<p>
<ul> <u>Web apps</u> -
    <li>Web apps, on the other hand, are accessed via the internet browser and will adapt to whichever device you’re viewing them on.</li>
    <li>They are not native to a particular system and don’t need to be downloaded or installed.</li>
    <li>Due to their responsive nature, they do indeed look and function a lot like mobile apps.</li>
</ul>
</p>



<h2>Pros of responsive web apps and cons of mobile apps:</h2>
<p>
    <ol>
        <li>It lowers the cost of production(Ready to use websites given by frameworks like Bootstrap eliminates designers costs)
        </li>
        <li>You have cross-platform accessibility
        </li>
        <li>There’s no need to install anything on your device
        </li>
        <li>No need to update the apps. Apps work only if installed and require updates for any changes.
        </li>
        <li>It reduces time to market(no approvals from app stores needed)
        <li>The content is more shareable(URL sharing)
        <li>Content is indexed by Google and other search engines(SEOs)
        <li>The number of mobile apps has reached over four million on the App Store and Google Play Store combined, it’s a challenge to get people to download new apps.        
        </li>
    </ol>
    <img src="images/mobile_cons.png" class="tablet" alt="" media="all" />
</p>

<h2>Cons of responsive web apps and Pros of mobile apps:</h2>
<p>
    <ol>
        <li>Web apps require an internet connection.
        </li>
        <li>You need to optimize content for mobile(images, animations) on web apps.
        </li>
        <li>There’s limited support of native device features(camera, microphone, push notifications) on web apps.
        </li>
        <li>Web cannot be distributed through an app store.
        </li>
        <li>Mobile apps are less laggy than web apps.The mobile app user experience is smooth and convenient, and this influences conversions, satisfaction rates and your brand reputation.
        <li>Acquiring users is more challenging for a mobile app than for websites. On the other hand, engagement is usually higher with a mobile app because of the value an app can bring:
            <ul>
                <li>Access from anywhere</li>
                <li>Geolocation features </li>
                <li>QR code scanners</li>
                <li>Augmented reality</li>
            </ul>
        <li>Mobile apps are more user friendly.Unlike websites that rely on browsers to support functions, mobile applications can be designed to support swipe gestures like a drag, pinch, hold, tap, double tap and more, each performing a certain action that increases user friendliness.
        <li>Safe and secure - native apps must first be approved by the app store.        
        </li>
    </ol>


<h3><u>Decision making between the both options would depend upon on the following questions:</u></h3>
<ol>
    <li>Do you plan to launch a full-fledged product?
        <ul>Full-fledged products are services that can’t exist outside of a mobile app: for example, Uber or any other vehicle sharing service. Instagram would also make little sense as a website. If your startup idea is based on mobile technology, you probably already know it. But what should you do if you have a food delivery business? A website can work as well as a mobile app: it just depends on your target audience. But if you want to implement a full-fledged service that will not only facilitate your communication with customers but also provide them a convenient tool for anywhere and anytime, you’ll need a mobile app.</ul>
    </li>

    <li>Do you plan to update your product often?
        <ul>If the answer yes, then go for a mobile app.</ul>
    </li>

    <li>Do you already have a big customer base?
        <ul>If you’re a startup and you just plan to launch your business that’s not based specifically on mobile technology, it’s often better to start with a responsive website.
        If you already have a successful online or offline business and you’d like to open yourself to a new market of mobile users, you should go for an app.
        </ul>
    </li>

    <li>Will you need offline mode?
    <ul>If yes, go for a mobile app.
        </ul>
    </li>
</ol>

 

