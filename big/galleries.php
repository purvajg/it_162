<h3>What are jQuery sliders:</h3>
<p>
    <ul>
    jQuery sliders offer a simple, elegant way to display content without
    ing up valuable real estate on the web page.
<li>An image on the web just had one way of showing up: as a flat JPG, GIF or 
PNG before using jQuery sliders. By using jQuery display multiple content 
items can be displayed in a shared space on the page.</li>
<li>The slider goes by a wide array of aliases: gallery, carousel, promos and 
rotators but they all provide the same general feature.
jQuery sliders can be used for nearly anything but most are used to 
display a number of photos in a slideshow format.</li>
<li>Many offer a number of 
transition effects between slides and can be controlled by previous/next 
buttons, tabbed or thumbnail navigation</li>
</ul>
</p>

<ul>
    <p>Top 3 Styles of jQuery Sliders according to our usage:</p>
    <li>Standard Photo Slider</li>
    <li>Mixed Content Slider</li>
    <li>Gallery Style Slider</li>
</ul>


<h3>Choosing the slider:</h3>
<p>Choosing a slider is largely dependent on the functionality and options you need and the overall style you are going for. Decide on the type of content you need displayed, how you want users to navigate through it and see which slider best matches your requirements. 
Once you have narrowed down your choices, compare the overall script 
size and check whether it is compatible with all browsers.
Some sliders are offered in a "lite" version such as jQuery Cycle Lite if you're looking to keep the overall file size down in your project. Oftentimes, the light version is just missing the additional transition effects, sticking with the most popular options available.
 </p>

